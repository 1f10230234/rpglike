  a = 0
  wid = 0
  time = 0
  
  while a < len(text):
    if text[a] == "/":
      command = "0"
      a += 1
      while text[a] != "/":
        command += text[a]
        a += 1 
      print_text(int(command),nx,ny,sz)
      a += 1
      continue
    elif text[a] == "*":
      a += 1
